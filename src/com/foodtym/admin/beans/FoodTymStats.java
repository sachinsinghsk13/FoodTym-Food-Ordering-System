package com.foodtym.admin.beans;

public class FoodTymStats {
	private int restaurants;
	private int foodItems;
	private int customers;
	private int deliveryPerson;
	private int localities;
	private int orderWaiting;
	private String adminName;
	public String getAdminName() {
		return adminName;
	}
	public void setAdminName(String adminName) {
		this.adminName = adminName;
	}
	public int getRestaurants() {
		return restaurants;
	}
	public void setRestaurants(int restaurants) {
		this.restaurants = restaurants;
	}
	public int getFoodItems() {
		return foodItems;
	}
	public void setFoodItems(int foodItems) {
		this.foodItems = foodItems;
	}
	public int getCustomers() {
		return customers;
	}
	public void setCustomers(int customers) {
		this.customers = customers;
	}
	public int getDeliveryPerson() {
		return deliveryPerson;
	}
	public void setDeliveryPerson(int deliveryPerson) {
		this.deliveryPerson = deliveryPerson;
	}
	public int getLocalities() {
		return localities;
	}
	public void setLocalities(int localities) {
		this.localities = localities;
	}
	public int getOrderWaiting() {
		return orderWaiting;
	}
	public void setOrderWaiting(int orderWaiting) {
		this.orderWaiting = orderWaiting;
	}
	
	
}
